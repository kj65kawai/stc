<p>
　本講義では、下記のように講義と演習をくみあわせ、
情報技術（ＩＣＴ）に関する基礎的概念の理解と基本的技術の習得を目標に、
教科書「ネットワーク社会における情報の活用と技術（三訂版）」
・「同学習ノート（三訂版）」の内容を順にすすめていく。
同時に、
教科書「Introduction to Information and Communication Technology.」の内容を、
適宜とりあげていく。
</p>

<p>授業の構成<ul>
<li> １回の授業で、おおむね教科書のひとつの節（1.1、1.2、…）をとりあげる。
<li> 授業は、講義と演習を組み合わせてすすめていく。
<li> 基本的には、９０分の授業時間のうち、 はじめの３０分間で、
演習の発表・議論を行ない、 つづく６０分間で、
その回の節のポイントとなる内容について講義を行なう。 
</ul></p>

<p>演習<ul>
<li> 演習課題は、基本的に、教科書「学習ノート」にある問題である。
<li> 演習では、各自、演習課題をレポートするとともに、発表担当者が発表する。
発表に際しては、必ず ppt 資料を作成し、
同資料を用いてプレゼンテーションを行なう。
<li> 発表後、議論を行なう。
<li> 発表に用いた ppt 資料は、
 Moodle の学生プレゼンテーションにアップロードし、
互いに参照できるようにする。
<li> 例えば、教科書の1.1節をとりあげる回の演習では、
受講生各自が、あらかじめ1.1節の演習課題を解き、
そのレポートを授業開始時に提出し、発表担当者が授業の最初に発表を行なう。
すなわち、演習のためのレポート作成・発表準備は、いわゆる「予習」にあたる。 
</ul></p>

<p>レポート<ul>
<li> レポートは、あらかじめ用意されたフォーマットの Word 文書に、
必要な内容を記入することによって作成し、その印刷したものを提出する。
</ul></p>

<p>講義<ul>
<li> 講義では、演習で学習した内容を踏まえ、
その回の節のポイントとなる内容・トピックをとりあげる。
<li> 講義の最後に、ミニレポートを作成し、提出する。
</ul></p>

<p>Moodle<ul>
<li> 本講義では、ｅラーニングシステムのひとつである
 <a href="https://lms.imc.tut.ac.jp/">Moodle</A> を、
プレゼンテーション資料や一部レポートの提出に用いる。
</ul></p>

<p>タイピング<ul>
<li> タイピングは、もっとも基本的な情報技術のひとつである。
本講義では、本学で作成したタイピング練習ソフトを使用して、
タッチタイピング技術の習得を行なう。
</ul></p>

<p>成績<ul>
<li> 受講状況（授業への参画度、プレゼンテーション、
質疑応答、レポート、ミニレポート、タイピング）【５０％】、
期末試験【５０％】をもとに、
本講義の<a href="jugyou-shoukai.pdf">目標</a>を、
どの程度達成したかを判定し、成績をつける。
</ul></p>

